#include <iostream>
#include <string>
#include <math.h>

using namespace std;

int digit_sum(string str) {
    int result = 0;
    for(char s : str) {
        result += (s - '0');
    }
    return result;
}
int main() {
    string s = "1071508607186267320948425049060001810561404811705533607443"
       "750388370351051124936122493198378815695858127594672917553146"
       "825187145285692314043598457757469857480393456777482423098542107460"
       "5062371141877954182153046474983581941267398767559165543946077"
       "062914571196477686542167660429831652624386837205668069376";

    cout << digit_sum(s) << endl;
	return 0;
}
